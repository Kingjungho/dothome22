            영국에서였는데, 영어 veget(able)과 '-arian'을 조합한 것이다.
            채소라는 뜻의 vegetable은 'veget'과 'able'이 결합하여 만들어진 것으로 able의 뜻은 원래 handle, held였으나 현대에는 capable이라는 뜻을 가지게 된다.
            그리고 veget는 라틴어 vegetus에서 유래하였는데 그 뜻은 vigorous(원기왕성한), energetic(정력적인), lively(생기에 넘친), move(움직이다),
            excite(일으키다)이다.
            채식주의(vegetarianism)라는 용어는 vegetarian이 고안된 이후에 만들어져 '고기를 먹지 않는 것의 원리(principle) 또는 실천(practice)'이라는 뜻으로
            1851년에 영어 vegetarian과 '-ism'이 결합하여 만들어졌다.
            채식주의는 비교적 최근에 만들어진 단어로 생선, 고기, 가금류를 식품에서 사용하는 것을 제외하는 것에 적용되며 1847년부터 쓰이기 시작하였다.
            한편 고기뿐만 아니라 계란과 우유도 먹지 않는 의미의 vegan은 1944년 영국의 도널드 왓슨에 의해 고안되었다. 처음에는 채식단체의 소그룹으로 진행을 하려 하였으나, 거절되자 독립적인 그룹을
            만들게 된다.
            그들은 스스로 무엇이라 부를까 고민하였는데 'veg(etarian)'+'an(사람을 뜻하는 접미사)'을 결합하여 vegan이라는 단어를 고안해 내고 비건 협회를 창설하게 된다.
